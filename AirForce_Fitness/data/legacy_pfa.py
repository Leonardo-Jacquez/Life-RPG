def _linear_table(max_reps, min_reps, max_pts=20.0, min_pts=1.0):
    rows = []
    for r in range(max_reps, min_reps - 1, -1):
        frac = (r - min_reps) / max(max_reps - min_reps, 1)
        rows.append((r, round(min_pts + frac * (max_pts - min_pts), 1)))
    rows.append((0, 0.0))
    return rows


